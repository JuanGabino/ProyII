package Beans;

public class Observaciones {
	
	private String idobservaciones;
	private String obervaciones;
	private String alumno;
	private String fecha;
	private String seccion;
	private String curso;
	private String docente;
	
	
	public String getIdobservaciones() {
		return idobservaciones;
	}
	public void setIdobservaciones(String idobservaciones) {
		this.idobservaciones = idobservaciones;
	}
	public String getObervaciones() {
		return obervaciones;
	}
	public void setObervaciones(String obervaciones) {
		this.obervaciones = obervaciones;
	}
	public String getAlumno() {
		return alumno;
	}
	public void setAlumno(String alumno) {
		this.alumno = alumno;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getSeccion() {
		return seccion;
	}
	public void setSeccion(String seccion) {
		this.seccion = seccion;
	}
	public String getCurso() {
		return curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	public String getDocente() {
		return docente;
	}
	public void setDocente(String docente) {
		this.docente = docente;
	}
	
	

}
